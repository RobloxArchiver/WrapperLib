local td = {}

-- Important Vars that go with the game :)
-- Note all of these except RService are game specific!
local GEvents = GUIEvents
local CEvents = CustomisationEvents
local Events = Events
local RService =  game:GetService("ReplicatedStorage")

-- void
function td.ChooseBaby()
    RService.GEvents.ChooseBaby:FireServer()
end

-- void
function td.SpawnBaby()
    RService.GEvents.SpawnBaby:FireServer()
end

-- string
-- Vals: "None", "Toddler" (Will find more perks!)
function td.ChangePerk(PerkName)
    RService.CEvents.ChangePerk:FireServer(PerkName)
end

-- void
function td.ChooseCaretaker()
    RService.GEvents.ChooseCaretaker:FireServer()
end

-- void
function td.SpawnCaretaker()
    RService.GEvents.SpawnCaretaker:FireServer()
end

-- String
function td.ChangeName(Name)
    RService.GEvents.ChangeName:FireServer(Name)
end

-- void
function td.ResetCharacter()
    RService.GEvents.ResetCharacter:FireServer()
end

-- Boolean Value (True/False)
function td.isCrawling(BooleanVal)
    RService.Events.Crawl:FireServer(BooleanVal)
end

-- Boolean Value (True/False)
function td.isCart(BooleanVal)
    RService.Events.Cart:FireServer(BooleanVal)
end

return td